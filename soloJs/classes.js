

function Player(name, playerIndex){

    this.name = name;
    this.playerIndex = playerIndex;

}



function SquareElement(name, type, color, value, landLord){

    this.name = name;
    
    this.type = type;
    
    this.value = value;

    this.color = color;

    this.landLord = landLord;

}



function Card(type, name, value){

    this.type = type;
    this.name = name;
    this.value = value;

}




